my_list = [10, 20, 30, 40, 50]
# Accessing elements using positive indices
first_element = my_list[0]
# Accesses the first #element (10)
second_element = my_list[1]  # Accesses the second #element (20)
# Accessing elements using negative indices
last_element = my_list[-1]
# Accesses the last #element (50)
second_to_last = my_list[-2]  # Accesses the second-#to-last element(40)
# Utilizing slicing to retrieve a specified range of # elements.
# Accesses elements from index 1 to 3 ([20, 30, 40])
sliced_elements = my_list[1:4]
print(first_element)
print(second_element)
print(last_element)
print(second_to_last)
print(sliced_elements)
