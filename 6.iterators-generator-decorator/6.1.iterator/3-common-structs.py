# 1. list
# List iteration
my_list = [1, 2, 3, 4, 5]
for item in my_list:
    print(item)
# Output:
# 1
# 2
# 3
# 4

# 2. dictionaries
my_dict = {'a': 3, 'b': 4, 'c': 5}
for key in my_dict:
    print(key, my_dict[key])
# Output:
# a 3
# b 4
# c 5

# 3. sets
my_set = {5, 6, 7, 8, 9}
for element in my_set:
    print(element)
# Output:
# 5
# 6
# 7
# 8
# 9

# 4. strings
my_string = "Hello"
for char in my_string:
    print(char)
# Output:
# H
# e
# l
# l
# o
